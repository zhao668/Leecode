public class LinkedListIntersect {
    public static ListNode getIntersectionNode(ListNode headA, ListNode headB) {
        ListNode curA = headA;
        ListNode curB = headB;
        int lenA = 0, lenB = 0;
        while (curA != null) {
            lenA++;
            curA = curA.next;
        }
        while (curB != null) {
            lenB++;
            curB = curB.next;
        }
        curA = headA;
        curB = headB;
        if (lenB > lenA) {
            int tmpLen = lenA;
            lenA = lenB;
            lenB = tmpLen;
            ListNode tmpNode = curA;
            curA = curB;
            curB = tmpNode;
        }
        int gap = lenA - lenB;
        while (gap-- > 0) {
            curA = curA.next;
        }
        while (curA != null) {
            if (curA == curB) {
                return curA;
            }
            curA = curA.next;
            curB = curB.next;
        }
        return null;
    }

    public static void main(String[] args) {
        ListNode listA = ListNode.createLinkedList(new int[]{0, 9, 1});
        ListNode listB = ListNode.createLinkedList(new int[]{3});
        ListNode listC = ListNode.createLinkedList(new int[]{2, 4});
        while (listA.next != null) {
            listA = listA.next;
        }
        listA.next = listC;
        while (listB.next != null) {
            listB = listB.next;
        }
        listB.next = listC;
        ListNode.printLinkedList(LinkedListIntersect.getIntersectionNode(listA, listB));
    }
}

